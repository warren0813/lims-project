const clone = <T,>(value: T): T => JSON.parse(JSON.stringify(value))

export const demoData = {
  experimentTypes: () => clone(experiments),
  equipment: () => clone(equipment),
  recipes: () => clone(recipes),
  requests: () => clone(requests),
  requestDetail: (id: number): RequestDetail => {
    const request = requests.find((r) => r.id === id) ?? requests[0]
    return clone({
      ...request,
      experiment_types: experiments.filter((e) => request.expIds.includes(e.id)),
      completed_at: request.status === "completed" ? request.updated : null,
      closed_at: null,
    })
  },
  samples: () => clone(samples),
  sample: (id: number) => clone(samples.find((s) => s.id === id) ?? samples[0]),
  wips: () => clone(wips),
  wip: (id: number) => clone(wips.find((w) => w.id === id) ?? wips[0]),
  dispatches: () => clone(dispatches),
  dispatch: (id: number) => clone(dispatches.find((d) => d.id === id) ?? dispatches[0]),
}
